package com.training.javacourse.aula11;

public class IntVariables {

	public static void main(String[] args) {
		
		byte age01 = 20;
		short age02 = 21;
		int age03 = 22;
		long age04 = 24;
		
		System.out.println("Age 01: " + age01);
		System.out.println("Age 02: " + age02);
		System.out.println("Age 03: " + age03);
		System.out.println("Age 04: " + age04);

	}

}
