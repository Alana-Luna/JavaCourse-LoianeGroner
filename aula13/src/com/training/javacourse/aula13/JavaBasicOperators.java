package com.training.javacourse.aula13;

public class JavaBasicOperators {

	public static void main(String[] args) {
		
		
		int result = 1 + 2;
		//3
		System.out.println(result);
		
		
		result = result - 1;
		//2
		System.out.println(result);
		
		
		result = result * 2;
		//4
		System.out.println(result);
		
		
		result = result/2;
		//2
		System.out.println(result);
		
		
		result = result%2;
		//0
		System.out.println(result);
		
		
		
		result++;
		//1 
		System.out.println(result);
		
		//print result and after will add 1
		//1
		System.out.println(result++);
		
		
		++result;
		//3
		System.out.println(result);
		
		//add 1 and print result
		//4
		System.out.println(++result);

	}

}
