package com.training.javacourse.aula16;


public class LoopDoWhile {

	public static void main(String[] args) {
		
	int number = 1;
	
	do{
		number++;
		System.out.println("Value: " + number);
		
		
	}while(number <= 10); 
	
	//the loop while check the condition before proceed
	//the loop do while first proceed and them check the condition
	//that's the difference between while and do while
	//that's why the 11 will be printed even the condition being number <=10 
	
	
		

	}

}
