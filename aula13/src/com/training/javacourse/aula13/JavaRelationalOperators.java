package com.training.javacourse.aula13;

public class JavaRelationalOperators {

	public static void main(String[] args) {
		
		int x = 1;
		int y = 2;
		
		System.out.println("X == Y: "+  (x == y));
		System.out.println("X != Y: " + (x != y));
		System.out.println("X > Y: " + (x > y));
		System.out.println("X < Y: " + (x < y));
		System.out.println("X >= Y: " + (x >= y));
		System.out.println("X <= Y: " + (x <= y));

	}

}
